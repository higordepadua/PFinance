    public void init() {
        documentoCorrente = new Documento();
        Date currDate = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        txtDataCriacao.setText(sdf.format(currDate.getTime()));
        txtNome.setText("");
    }

    public void editar(Documento documento) {
        this.setDocumentoCorrente(documento);
        txtNome.setText(documento.getNome());
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        txtDataCriacao.setText(sdf.format(documento.getDataCriacao()));
    }

