    private void salvar(Documento doc) {
        this.modificado = true;
    }

    private Documento getFormData(){
        documentoCorrente.setNome(txtNome.getText());
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        java.sql.Date data = null;
        try {
            data = new java.sql.Date(format.parse(txtDataCriacao.getText()).getTime());
        } catch (ParseException ex) {
            Logger.getLogger(FormDocumento.class.getName()).log(Level.SEVERE, null, ex);
        }
        documentoCorrente.setDataCriacao(data);
        return documentoCorrente;
    }

